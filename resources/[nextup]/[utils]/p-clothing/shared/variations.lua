
local function gloveRange(from, to, pattern)
    local t = {}
    local len = #pattern
    for i = from, to do
        t[i] = pattern[((i - from) % len) + 1]
    end
    return t
end


local function merge(...)
    local result = {}
    for _, tbl in ipairs({ ... }) do
        for k, v in pairs(tbl) do
            result[k] = v
        end
    end
    return result
end


